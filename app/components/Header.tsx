import {Await, NavLink, useLocation} from '@remix-run/react';
import {Suspense, useRef, useEffect} from 'react';
import type {HeaderQuery} from 'storefrontapi.generated';
import type {LayoutProps} from './Layout';
import {useRootLoaderData} from '~/root';


import { gsap } from 'gsap'
import ScrollTrigger from 'gsap/dist/ScrollTrigger'

import Menu from './Menu';
import MenuInterior from './MenuInterior';
import MobileMenu from './MobileMenu';
import MainLogo from './MainLogo';

type HeaderProps = Pick<LayoutProps, 'header' | 'cart' | 'isLoggedIn'>;

type Viewport = 'desktop' | 'mobile';

export function Header({header, isLoggedIn, cart}: HeaderProps) {

  const location = useLocation();
  const isHome = location.pathname === "/";

  const headStick = useRef<HTMLDivElement | null>(null);

  useEffect(() => {
    gsap.registerPlugin(ScrollTrigger);

    if (headStick.current) {
      gsap.to(headStick.current, {
        scrollTrigger: {
          trigger: '.mainHeader',
          endTrigger: 'html',
          scrub: 1,
          toggleClass: "sticky",
          start: "top 0px"
        }
      });
    }
  }, []);



  const {shop, menu} = header;
  return (
    <header 
      className={`always-flex justify mainHeader padding-10 ${isHome ? "imHome" : "imNotHome"}`}
      ref={headStick}
    >
      <div className="mainLogo">
        <NavLink className="logoLink flex-vertical" to="/">
            <MainLogo />
        </NavLink>
      </div>

      <div className="desktopNav">
        {isHome ? <Menu /> : <MenuInterior />}
      </div>

      <HeaderCtas isLoggedIn={isLoggedIn} cart={cart} />
    </header>
  );
}

export function HeaderMenu({
  menu,
  primaryDomainUrl,
  viewport,
}: {
  menu: HeaderProps['header']['menu'];
  primaryDomainUrl: HeaderQuery['shop']['primaryDomain']['url'];
  viewport: Viewport;
}) {
  const {publicStoreDomain} = useRootLoaderData();
  const className = `header-menu-${viewport}`;

  function closeAside(event: React.MouseEvent<HTMLAnchorElement>) {
    if (viewport === 'mobile') {
      event.preventDefault();
      window.location.href = event.currentTarget.href;
    }
  }

  return (
    <nav className={className} role="navigation">
      {viewport === 'mobile' && (
        <NavLink
          end
          onClick={closeAside}
          prefetch="intent"
          style={activeLinkStyle}
          to="/"
        >
          Home
        </NavLink>
      )}
      {(menu || FALLBACK_HEADER_MENU).items.map((item) => {
        if (!item.url) return null;

        // if the url is internal, we strip the domain
        const url =
          item.url.includes('myshopify.com') ||
          item.url.includes(publicStoreDomain) ||
          item.url.includes(primaryDomainUrl)
            ? new URL(item.url).pathname
            : item.url;
        return (
          <NavLink
            className="header-menu-item"
            end
            key={item.id}
            onClick={closeAside}
            prefetch="intent"
            style={activeLinkStyle}
            to={url}
          >
            {item.title}
          </NavLink>
        );
      })}
    </nav>
  );
}

function HeaderCtas({
  //isLoggedIn,
  cart,
}: Pick<HeaderProps, 'isLoggedIn' | 'cart'>) {
  return (
    <nav className="header-ctas" role="navigation">
      <MobileMenu />
      <CartToggle cart={cart} />
    </nav>
  );
}

